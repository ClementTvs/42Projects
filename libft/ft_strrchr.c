char	*ft_strrchr(char *str, int c)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == c && ft_strrchr(str + i + 1, c) == 0)
		{
			return (str + i);
		}
		i++;
	}
	return (0);
}
